number_1 = int(input())
number_2 = int(input())

print(number_1 > number_2)   # prints true if number_1 is greater than number_2
print(number_1 < number_2)   # prints true if number_1 is less than number_2
print(number_1 >= number_2)  # prints true if number_1 is greater than equal to number_2
print(number_1 <= number_2)  # prints true if number_1 is less than equal to number_2